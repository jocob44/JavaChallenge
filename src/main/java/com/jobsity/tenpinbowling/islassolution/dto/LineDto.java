package com.jobsity.tenpinbowling.islassolution.dto;

public class LineDto {

    private String name;
    private String number;

    public LineDto(String name, String number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
